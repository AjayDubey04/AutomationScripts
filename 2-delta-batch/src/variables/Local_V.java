package variables;

public class Local_V {

//	This is Local Variable Concept //
//	We can use final key word with Local Variable //
	
	
	
	public static void main(String[] args) {
		
		
		String add="Hello";
		System.out.println(add);
		
		
		int a=5;
		int b=5;
		System.out.println(a+b);
		
		Final();
		
		int c=5;
		String name="Java";
		String sum=name+c;
		System.out.println(sum);
		

	}
	public static void Final() {
		
		
		int num=24354;
		int sum=3456;
		final int add=num+sum;
		System.out.println(add);
	
		
		
	}

}
